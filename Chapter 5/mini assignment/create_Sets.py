#Create a Set of number and show union and intersection with another set
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}
print("Set 1 is :", set1)
print("Set 2 is :", set2)
# Union of two sets
print("Union of Set1 and Set2 is : ",set1.union(set2))

# Intersection of two sets
print("Intersection of Set1 and Set2 is : ",set1.intersection(set2))
