input = float(input("Enter temperature in Celsius( °C ): "))
print("input Temperature in Celsius(°C) is :", input,"°C")
Fahrenheit = (input * 9/5) + 32
print ("Temperature in Fahrenheit(°F):", Fahrenheit,"°F")# Simple Temperature Converter from Celsius to Fahrenheit
Kelvin = input + 273.15
print ("Temperature in Kelvin:", Kelvin,"K") # Simple Temperature Converter from Celsius to Kelvin
